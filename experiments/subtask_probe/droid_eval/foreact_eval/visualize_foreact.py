#!/usr/bin/env python3
"""Render exterior | wrist | predicted foresight | subtask per DROID frame.

Extends ``visualize_subtasks.py`` with a third image column that pulls the
foresight PNG generated by ``generate_foresight.py``. If a foresight image is
missing for a given frame (e.g. the generator skipped it), a grey placeholder
with the text "(no foresight)" is shown so the row still aligns.

Usage::

    uv run python -m experiments.subtask_probe.droid_eval.foreact_eval.visualize_foreact \\
        --samples_dir ./.experiments_cache/droid_eval_ah15 \\
        --subtasks ./.experiments_cache/droid_eval_ah15/subtasks_foreact_qwen8b.json \\
        --foresight_dir ./.experiments_cache/droid_eval_ah15/foresight_foreact \\
        --output_dir ./.experiments_cache/droid_eval_ah15/foreact_report \\
        [--video --fps 2]
"""

from __future__ import annotations

import argparse
import logging
from pathlib import Path

import imageio.v3 as iio3
import numpy as np
from PIL import Image

from experiments.subtask_probe.droid_eval.utils import load_manifest, load_subtask_index
from experiments.subtask_probe.droid_eval.visualize_subtasks import (
    _composite_side_by_side,
    _draw_caption,
    _encode_png_base64,
    _load_frame_images,
)

logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
logger = logging.getLogger(__name__)


def _load_foresight_image(
    foresight_dir: Path, episode_id: str, frame_idx: int, target_h: int, target_w: int
) -> np.ndarray:
    """Load the foresight PNG for a given frame, resizing to (target_h, target_w).

    Returns a grey placeholder if the PNG is missing so the HTML/video grid
    stays aligned even when the generator skipped frames.
    """
    path = foresight_dir / episode_id / f"frame_{frame_idx:05d}.png"
    if not path.exists():
        return np.full((target_h, target_w, 3), 40, dtype=np.uint8)
    with Image.open(path) as img:
        resized = img.convert("RGB").resize((target_w, target_h), Image.Resampling.LANCZOS)
        return np.asarray(resized, dtype=np.uint8)


def _write_html(
    out_path: Path,
    episode_id: str,
    instruction: str,
    items: list[tuple[int, str, dict[str, np.ndarray]]],
) -> None:
    """Emit a self-contained HTML with exterior | wrist | foresight | subtask."""
    rows = []
    for frame_idx, subtask, views in items:
        ext_b64 = _encode_png_base64(views["exterior"])
        wrist_b64 = _encode_png_base64(views["wrist"])
        fore_b64 = _encode_png_base64(views["foresight"])
        rows.append(
            f"<tr>"
            f"<td class='idx'>{frame_idx}</td>"
            f"<td class='img'><div class='cam-label'>exterior</div>"
            f"<img src='data:image/png;base64,{ext_b64}' width='260'/></td>"
            f"<td class='img'><div class='cam-label'>wrist</div>"
            f"<img src='data:image/png;base64,{wrist_b64}' width='260'/></td>"
            f"<td class='img'><div class='cam-label'>predicted foresight</div>"
            f"<img src='data:image/png;base64,{fore_b64}' width='260'/></td>"
            f"<td class='sub'>{subtask or '<em>(empty)</em>'}</td>"
            f"</tr>"
        )
    html = (
        "<!doctype html><html><head><meta charset='utf-8'>"
        f"<title>ForeAct \u2014 {episode_id}</title>"
        "<style>"
        "body{font-family:system-ui,sans-serif;margin:2rem;background:#0b0f14;color:#e6edf3}"
        "h1{font-size:1.1rem;margin-bottom:.25rem}"
        "p.meta{color:#8b949e;margin-top:0}"
        "table{border-collapse:collapse;margin-top:1rem}"
        "td{padding:8px;vertical-align:middle;border-bottom:1px solid #30363d}"
        "td.idx{color:#8b949e;font-family:ui-monospace,Menlo,Consolas,monospace;width:60px}"
        "td.sub{font-size:1rem;max-width:380px}"
        "td.img{padding:8px}"
        ".cam-label{color:#8b949e;font-size:.75rem;margin-bottom:4px;"
        "text-transform:uppercase;letter-spacing:.05em}"
        "img{border-radius:6px;display:block}"
        "</style></head><body>"
        f"<h1>{episode_id}</h1>"
        f"<p class='meta'>Instruction: <code>{instruction}</code> &middot; {len(items)} frames</p>"
        "<table>"
        "<thead><tr><th>frame</th><th>exterior</th><th>wrist</th>"
        "<th>predicted foresight</th><th>subtask</th></tr></thead>"
        "<tbody>" + "".join(rows) + "</tbody></table>"
        "</body></html>"
    )
    out_path.write_text(html)


def _write_mp4(
    out_path: Path,
    frames_with_captions: list[np.ndarray],
    fps: int,
) -> None:
    iio3.imwrite(
        out_path,
        np.stack(frames_with_captions),
        fps=fps,
        codec="libx264",
        macro_block_size=1,
    )


def _process_episode(
    samples_dir: Path,
    foresight_dir: Path,
    episode: dict,
    subtask_index: dict[tuple[str, int], str],
    output_dir: Path,
    video: bool,
    fps: int,
) -> None:
    episode_id = episode["episode_id"]
    instruction = episode["instruction"]

    items: list[tuple[int, str, dict[str, np.ndarray]]] = []
    captioned: list[np.ndarray] = []

    for frame_info in episode["frames"]:
        frame_idx = frame_info["frame_idx"]
        views = _load_frame_images(samples_dir / frame_info["file"])
        ext_h, ext_w = views["exterior"].shape[:2]
        foresight = _load_foresight_image(foresight_dir, episode_id, frame_idx, ext_h, ext_w)
        views["foresight"] = foresight
        subtask = subtask_index.get((episode_id, frame_idx), "")
        items.append((frame_idx, subtask, views))
        if video:
            composite = _composite_side_by_side(views)
            ext_w = views["exterior"].shape[1]
            wrist_w = views["wrist"].shape[1]
            separator = 4
            captioned.append(
                _draw_caption(
                    composite,
                    caption=subtask,
                    footer=f"{episode_id} frame {frame_idx}  |  task: {instruction}",
                    camera_labels=[
                        (4, "EXTERIOR"),
                        (ext_w + separator + 4, "WRIST"),
                        (ext_w + wrist_w + 2 * separator + 4, "FORESIGHT"),
                    ],
                )
            )

    if video:
        mp4_path = output_dir / f"{episode_id}.mp4"
        _write_mp4(mp4_path, captioned, fps=fps)
        logger.info("%s -> %s (%d frames, %d fps)", episode_id, mp4_path, len(captioned), fps)
    else:
        html_path = output_dir / f"{episode_id}.html"
        _write_html(html_path, episode_id, instruction, items)
        logger.info("%s -> %s (%d frames)", episode_id, html_path, len(items))


def main() -> None:
    parser = argparse.ArgumentParser(description="Render ForeAct foresight + subtask grid")
    parser.add_argument("--samples_dir", type=str, required=True)
    parser.add_argument("--subtasks", type=str, required=True)
    parser.add_argument(
        "--foresight_dir",
        type=str,
        required=True,
        help="Directory containing foresight/{episode_id}/frame_{idx:05d}.png files.",
    )
    parser.add_argument("--output_dir", type=str, required=True)
    parser.add_argument("--video", action="store_true")
    parser.add_argument("--fps", type=int, default=2)
    args = parser.parse_args()

    samples_dir = Path(args.samples_dir)
    foresight_dir = Path(args.foresight_dir)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    manifest = load_manifest(samples_dir)
    subtask_index = load_subtask_index(Path(args.subtasks))
    logger.info(
        "Loaded %d episodes, %d subtask records, foresight_dir=%s",
        len(manifest),
        len(subtask_index),
        foresight_dir,
    )

    for episode in manifest:
        _process_episode(
            samples_dir=samples_dir,
            foresight_dir=foresight_dir,
            episode=episode,
            subtask_index=subtask_index,
            output_dir=output_dir,
            video=args.video,
            fps=args.fps,
        )

    logger.info("Done. Output: %s", output_dir)


if __name__ == "__main__":
    main()
